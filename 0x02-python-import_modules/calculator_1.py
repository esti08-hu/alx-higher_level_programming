#!/usr/bin/python3

# Define a function that adds two numbers.
def add(a, b):
    return (a + b)

# Define a function that subtracts two numbers.
def sub(a, b):
    return (a - b)

# Define a function that multiplies two numbers.
def mul(a, b):
    return (a * b)

# Define a function that divides two numbers.
def div(a, b):
    return int(a / b)